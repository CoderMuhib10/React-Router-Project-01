/* 
Steps to install React Router:
1.npm i react-router-dom    ----->run this command in your terminal.
2.import Link and Navlink(if u need) like this- import{Link,NavLink}from 'react-router-dom'
3.Delete App.jsx file
4.Make a jsx file you can give any name to it.let's say 'Layout.jsx'. 
Now in this Layout.jsx file import outlet like this- import { Outlet } from "react-router-dom";
now you can use <Outlet/> anywhere you want inside this file.
5.In main.jsx replace <app/> with <RouterProvider router={router}/>
6.Then import this- import { RouterProvider, createBrowserRouter } from 'react-router-dom'
7.Now you've to declare/make an array of object with the same name which you passed as props in <RouterProvider/> and those will be your routes.
ex:
const router=createBrowserRouter([

  {
    path:'/',
    element:<Layout/>,
    children:[
      {
        path:'',
        element:<Home/>
      
      },{
        path:'about',
        element:<About/>
      }
      ,{
        path:'contact',
        element:<Contact/>
      }
    ]
  }
])
----------------------
or,Instead of above syntax you can also write like this-

const router = createBrowserRouter(
  createRoutesFromElements(
    <Route path="/" element={<Layout />}>
      <Route path="" element={<Home />} />
      <Route path="about" element={<About />} />
      <Route path="contact" element={<Contact />} />
    </Route>
  )
);

**you can use any of these syntaxes which one you like to define your routes.
*/
